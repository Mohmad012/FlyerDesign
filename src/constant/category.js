export const getAsideDealsData = (params, categoryData) => {
  return {
    name:
      categoryData?.data?.children_data?.filter(category =>
        String(category?.url_key)?.includes(params?.category[0])
      )[0]?.name || '',
    description:
      params?.category?.length > 1
        ? categoryData?.data?.children_data?.filter(category =>
            String(category?.url_key)?.includes(params?.category[0])
          )[0]?.description || ''
        : categoryData?.data?.description,

    listOfSubProduct: categoryData?.data?.children_data
      ?.filter(category =>
        String(category?.url_key)?.includes(params?.category[0])
      )[0]
      ?.children_data?.map(item => ({
        name: item?.name,
        product_count: item?.product_count,
        id: item?.id,
        url_key: item?.url_key,
        parent_url_key: params?.category[0],
      })),
  }
}

export const getImagesSlides = (params, categoryData) => {
  return (
    categoryData?.data?.children_data
      ?.filter(img => String(img?.url_key).includes(params?.category[0]))[0]
      ?.children_data?.map(item => ({
        img: item?.category_desktop_image1,
        parent_id: item?.parent_id,
        id: item?.id,
        name: item?.name,
        url_key: item?.url_key,
        parentName: params?.category[0],
      }))
      ?.filter(item => item?.img != null)
      ?.map(item => ({
        ...item,
        img: process.env.webUrl + item?.img,
      })) || []
  )
}

